package CalinA.OCA8.JulOCA.Arrays.CreatingArray;

public class Jul25Multidimensionalarrays {
  public static void main(String[] args) {
    int[][] arr = {{5, 10}, {15, 20}};
    for (int x = 0; x < 2; x++)
      for (int y = 0; y < 2; y++) System.out.println("Array " + x + " = " + y + " = " + arr[x][y]);
  }
}
