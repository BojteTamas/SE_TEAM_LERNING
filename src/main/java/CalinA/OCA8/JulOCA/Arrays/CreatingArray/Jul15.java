package CalinA.OCA8.JulOCA.Arrays.CreatingArray;

public class Jul15 {
  public static void main(String[] args) {
    int[] arr;

    arr = new int[10];
    arr[0] = 15;
    arr[1] = 20;
    arr[2] = 25;
    arr[3] = 30;
    arr[4] = 35;

    for (int x = 0; x < arr.length; x++) System.out.println("Index " + x + " = " + arr[x]);
  }
}
